int		ft_bouton(int i, int j, int k)
{
	if ((i < k && i > j) || (i > k && i < j))
		return (i);
	if ((j < k && j > i) || (j > k && j < i))
		return (j);
	if ((k < i && k > j) || (k > i && k < j))
		return (k);
	else
		return (0);
}
