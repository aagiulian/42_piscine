int		ft_is_prime(int nb)
{
	int i;
	int	count;

	count = 0;
	i = 1;
	if (nb <= 0)
		return (0);
	while (i < nb)
	{
		if ((nb % i) == 0)
			count++;
		if (count > 1)
			return (0);
		i++;
	}
	return (1);
}
