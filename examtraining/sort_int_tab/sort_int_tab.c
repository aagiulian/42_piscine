void	sort_int_tab(int *tab, unsigned int size)
{
	int i;
	int swap;

	swap = 0;
	i = 0;
	while ((unsigned int)i < size - 1)
	{
		if ((tab[i] - tab[i + 1]) > 0)
		{
			swap = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = swap;
			i = -1;
		}
		i++;
	}
}
